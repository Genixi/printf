#include "libft.h"

void	ft_striter(char *s, void (*f)(char*))
{
	char *s_el;

	if (s == NULL || f == NULL)
		return ;
	s_el = s;
	while (*s_el != '\0')
	{
		f(s_el);
		s_el++;
	}
}
